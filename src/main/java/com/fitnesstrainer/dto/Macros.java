package com.fitnesstrainer.dto;

public class Macros {

    private int protein_g;
    private int carbs_g;
    private int fat_g;

    public int getProtein_g() {
        return protein_g;
    }

    public void setProtein_g(int protein_g) {
        this.protein_g = protein_g;
    }

    public int getCarbs_g() {
        return carbs_g;
    }

    public void setCarbs_g(int carbs_g) {
        this.carbs_g = carbs_g;
    }

    public int getFat_g() {
        return fat_g;
    }

    public void setFat_g(int fat_g) {
        this.fat_g = fat_g;
    }
}